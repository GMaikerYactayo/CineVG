package modelo;

public class Persona {

    private String IDPER;
    private String NOMPER;
    private String APEPER;
    private int DNIPER;
    private String USUPER;
    private String PSWPER;

    public String getIDPER() {
        return IDPER;
    }

    public void setIDPER(String IDPER) {
        this.IDPER = IDPER;
    }

    public String getNOMPER() {
        return NOMPER;
    }

    public void setNOMPER(String NOMPER) {
        this.NOMPER = NOMPER;
    }

    public String getAPEPER() {
        return APEPER;
    }

    public void setAPEPER(String APEPER) {
        this.APEPER = APEPER;
    }

    public int getDNIPER() {
        return DNIPER;
    }

    public void setDNIPER(int DNIPER) {
        this.DNIPER = DNIPER;
    }

    public String getUSUPER() {
        return USUPER;
    }

    public void setUSUPER(String USUPER) {
        this.USUPER = USUPER;
    }

    public String getPSWPER() {
        return PSWPER;
    }

    public void setPSWPER(String PSWPER) {
        this.PSWPER = PSWPER;
    }

    
}
